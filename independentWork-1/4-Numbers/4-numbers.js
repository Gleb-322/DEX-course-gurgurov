// задача 5 - Встроенный метод Math.random() возвращает случайное число от 0 (включительно) до 1 (но не включая 1)
// Напишите функцию random(min, max), которая генерирует случайное число с плавающей точкой от min до max (но не включая max).
// Решение
function random(min, max) {
    return min + Math.random() * (max - min)
}
console.log(random(1,5)) // 4.84185618144315865
console.log(random(1,5))  // 1.110705933976675866 
console.log(random(1,5)) // 3.204516649038948

// задача 6 - Напишите функцию randomInteger(min, max), которая генерирует случайное целое (integer) число от min до max (включительно).
// Решение
function randomInteger(min, max) {
    return Math.floor(min + Math.random() * (max + 1 - min))
}
console.log(randomInteger(1,5)) // 4
console.log(randomInteger(1,5))  // 5
console.log(randomInteger(1,5)) // 4