/* 
    задача 3 - Создайте функцию readNumber, которая будет запрашивать ввод числового значения до тех пор, 
    пока посетитель его не введёт.
    Функция должна возвращать числовое значение.
    Также надо разрешить пользователю остановить процесс ввода, 
    отправив пустую строку или нажав «Отмена». В этом случае функция должна вернуть null.
*/
// Решение 
function readNumber() {
    let enter
    while (!+(enter)) {
        enter = prompt('Введите число')
        if (enter === '' || enter === null) {
            return null
        }
        if (+enter) {
            return enter
        }
    }
    
}
alert(readNumber())

